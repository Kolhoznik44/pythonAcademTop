"""Задание 1
Пользователь вводит с клавиатуры три числа. В зависимости от выбора пользователя программа выводит
на экран сумму трёх чисел или произведение трёх чисел.
"""
a = float(input("Введите первое число: "))
b = float(input("Введите второе число: "))
c = float(input("Введите третье число: "))

print("Выберите операцию:")
print("1. Сумма трёх чисел")
print("2. Произведение трёх чисел")

choice = input("Введите номер операции (1 или 2): ")

if choice == '1':
    result = a + b + c
    print(f"Сумма чисел: {result}")
elif choice == '2':
    result = a * b * c
    print(f"Произведение чисел: {result}")
else:
    print("Некорректный выбор. Пожалуйста, введите 1 или 2.")

"""
Задание 2
Пользователь вводит с клавиатуры три числа. В зависимости от выбора пользователя программа выводит
на экран максимум из трёх, минимум из трёх или среднеарифметическое трёх чисел.
"""

a1 = float(input("Введите первое число: "))
b2 = float(input("Введите второе число: "))
c3 = float(input("Введите третье число: "))

# Запрашиваем выбор операции
print("Выберите операцию:")
print("1. Максимум из трёх чисел")
print("2. Минимум из трёх чисел")
print("3. Среднее арифметическое трёх чисел")

choice = input("Введите номер операции (1, 2 или 3): ")

if choice == '1':
    result = max(a1, b2, c3)
    print(f"Максимум из чисел: {result}")
elif choice == '2':
    result = min(a1, b2, c3)
    print(f"Минимум из чисел: {result}")
elif choice == '3':
    result = (a1 + b2 + c3) / 3
    print(f"Среднее арифметическое чисел: {result}")
else:
    print("Некорректный выбор. Пожалуйста, введите 1, 2 или 3.")

"""
Задание 3
Пользователь вводит с клавиатуры количество метров. В зависимости от выбора пользователя программа
переводит метры в мили, дюймы или ярды.
"""

meters = float(input("Введите количество метров: "))

print("Выберите единицу измерения для перевода:")
print("1. Мили")
print("2. Дюймы")
print("3. Ярды")

MILES_IN_A_METER = 1609.34
INCH_IN_A_METER = 0.0254
YARD_IN_A_METER = 0.9144

choice = input("Введите номер операции (1, 2 или 3): ")

if choice == '1':
    # Перевод метров в мили (1 миля = 1609.34 метров)
    result = meters / MILES_IN_A_METER
    print(f"{meters} метров = {result} миль")
elif choice == '2':
    # Перевод метров в дюймы (1 дюйм = 0.0254 метра)
    result = meters / INCH_IN_A_METER
    print(f"{meters} метров = {result} дюймов")
elif choice == '3':
    # Перевод метров в ярды (1 ярд = 0.9144 метра)
    result = meters / YARD_IN_A_METER
    print(f"{meters} метров = {result} ярдов")
else:
    print("Некорректный выбор. Пожалуйста, введите 1, 2 или 3.")